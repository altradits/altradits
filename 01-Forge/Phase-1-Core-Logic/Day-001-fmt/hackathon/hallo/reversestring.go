// Write a function that takes a string as an input and returns that string reversed.
package main

func reverseString(s string) string {
	runes := []rune(s)
	for i, j := 0, len(runes)-1; i < j; i, j = i+1, j-1 {
		runes[i], runes[j] = runes[j], runes[i]
	}
	return string(runes)
}

func main() {
	println(reverseString("Surgical Go"))
}
